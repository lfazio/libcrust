#pragma once

#include <stdint.h>
#include <stdlib.h>
#include <sys/types.h>

/**
 * @brief Typedef for an unsigned 8-bit integer.
 */
typedef uint8_t u8;

/**
 * @brief Typedef for an unsigned 16-bit integer.
 */
typedef uint16_t u16;

/**
 * @brief Typedef for an unsigned 32-bit integer.
 */
typedef uint32_t u32;

/**
 * @brief Typedef for an unsigned 64-bit integer.
 */
typedef uint64_t u64;

/**
 * @brief Typedef for a signed 8-bit integer.
 */
typedef int8_t i8;

/**
 * @brief Typedef for a signed 16-bit integer.
 */
typedef int16_t i16;

/**
 * @brief Typedef for a signed 32-bit integer.
 */
typedef int32_t i32;

/**
 * @brief Typedef for a signed 64-bit integer.
 */
typedef int64_t i64;

/**
 * @brief Typedef for an unsigned size type.
 */
typedef size_t usize;

/**
 * @brief Typedef for a signed size type.
 */
typedef ssize_t isize;